Dwarf minke whales mostly swim alone or in pairs, although groups may form at feeding sites. Minke whales are known to be curious, often approaching boats or swimmers. In the northern Great Barrier Reef area this inherently inquisitive behaviour has led to dwarf minke whales becoming the subject of a swim-with whale program managed by the Great Barrier Reef Marine Park Authority. The distribution of this species in Australian waters is not well understood but does extend at least from sub-Antarctic waters to waters off the northern Great Barrier Reef and north Western Australia.
